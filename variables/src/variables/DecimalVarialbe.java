package variables;

public class DecimalVarialbe {

	public static void main(String[] args) {
		
		// 코드 설명 작성 발법 ==> 주석 (Comment) --> Single Line Comment
		// 정수 변수 생성 실습
		// byte, short, int, long
		byte byteNumber = 10; // 1byte --> 256 ==> -127 ~ 126
		short shortNumber = 3300; // 2byte --> 약 -32000 ~ 약 32000
		int intNumber = 300_000_000; // 4byte --> 약 -21억 ~ 약 21억
		// 자바에서 정수 선언 시, 단위를 알아보기 위한 _(언더바) 추가 허용
		long longNumber = 4_000_000_000L; // 8byte --> 약 -900경 ~ 약 900경
		// long 타입은 Literal 필요(숫자 뒤에 l 또는 L 추가)
		
	}
}
